package week13homework;

public class ForloopTable {
    public static void main(String[] args) {

        /* To generate a table using For loop
        Generating table of 5
         */

        int n1 = 5;

        for (int n2 = 1; n2 <= 10; n2++) {

            System.out.println(n1 + " * " +n2+ " = " + n1 * n2);
        }
    }
}