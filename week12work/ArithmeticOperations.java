package week12work;

public class ArithmeticOperations {
    // To use Arithmetic Operators +,-,*,/,%


    public static void main(String [] args){
        float m1 = 9f;
        float m2 = 5f;

        System.out.println(m1+m2);
        System.out.println(m1-m2);
        System.out.println(m2-m1);
        System.out.println(m1*m2);
        System.out.println(m1/m2);
        System.out.println(m2/m1);
        System.out.println(m1%m2);

        // To use Unary Operators

        int I = 8;
        I++;
        System.out.println(I);

        I--;
        System.out.println(I);

        ++I;
        System.out.println(I);

        --I;
        System.out.println(I);

    }
}
